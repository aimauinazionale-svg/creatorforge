  /** @type {string} */
  let cachedLocale = FALLBACK;

  function detectBrowserLocale() {
    const lang = (navigator.language || "it").slice(0, 2).toLowerCase();
    return SUPPORTED.includes(lang) ? lang : FALLBACK;
  }

  /**
   * @param {string} [locale]
   */
  function normalizeLocale(locale) {
    const code = String(locale || "").slice(0, 2).toLowerCase();
    return SUPPORTED.includes(code) ? code : FALLBACK;
  }

  /**
   * @param {string} key
   * @param {string} [locale]
   * @param {Record<string, string | number>} [vars]
   */
  function t(key, locale, vars) {
    const loc = normalizeLocale(locale || cachedLocale);
    let str = MESSAGES[loc]?.[key] || MESSAGES.en[key] || key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
      }
    }
    return str;
  }

  /**
   * @param {number | null | undefined} n
   * @param {string} [locale]
   */
  function formatCompact(n, locale) {
    if (n == null || Number.isNaN(n)) return "—";
    const loc = normalizeLocale(locale || cachedLocale);
    try {
      return new Intl.NumberFormat(loc, { notation: "compact", maximumFractionDigits: 1 }).format(n);
    } catch {
      return String(n);
    }
  }

  /**
   * @param {number | null | undefined} n
   * @param {string} [locale]
   */
  function formatFull(n, locale) {
    if (n == null || Number.isNaN(n)) return "—";
    const loc = normalizeLocale(locale || cachedLocale);
    return new Intl.NumberFormat(loc).format(n);
  }

  async function loadLocaleFromStorage() {
    try {
      const stored = await chrome.storage.sync.get({ locale: "" });
      const raw = stored.locale;
      cachedLocale = raw ? normalizeLocale(raw) : detectBrowserLocale();
    } catch {
      cachedLocale = detectBrowserLocale();
    }
    return cachedLocale;
  }

  /**
   * @param {string} locale
   */
  function setLocale(locale) {
    cachedLocale = normalizeLocale(locale);
  }

  function getLocale() {
    return cachedLocale;
  }

  void loadLocaleFromStorage();

  if (typeof chrome !== "undefined" && chrome.storage?.onChanged) {
    chrome.storage.onChanged.addListener((changes, area) => {
      if (area === "sync" && changes.locale) {
        setLocale(changes.locale.newValue || detectBrowserLocale());
      }
    });
  }

